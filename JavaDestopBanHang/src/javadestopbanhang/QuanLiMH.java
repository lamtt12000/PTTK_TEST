    public void showData(){
        Vector col = new Vector();
        
        col.addElement("Mã Hàng");  
        col.addElement("Tên Hàng");
        col.addElement("Số Lượng");
        col.addElement("Đơn Giá (£)");
        col.addElement("Loại Hàng");
        
        
       Vector data = new Vector();
       
        try {
            String sql = "SELECT * FROM mat_hang";
            ps=con.prepareStatement(sql);
            rs=ps.executeQuery();
       while(rs.next()){
           Vector mh = new Vector();
                   
           mh.addElement(rs.getString("idmh"));
           mh.addElement(rs.getString("ten_hang"));
           mh.addElement(rs.getString("so_luong"));
           mh.addElement(rs.getString("don_gia"));
           mh.addElement(rs.getString("loai_hang"));
           
           data.add(mh);
          
       }
        } catch (Exception e) {
            e.printStackTrace();
        }
          tblMh.setModel(new DefaultTableModel(data,col));
    }
