    private void mnuXuatDSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnuXuatDSActionPerformed
        JFileChooser jfc = new JFileChooser();
        jfc.setCurrentDirectory(new File("D:\\user"));
        int temp = jfc.showSaveDialog(this);
        if(temp ==JFileChooser.APPROVE_OPTION){
            File f = jfc.getSelectedFile();
            try{
               
                    FileWriter out = new FileWriter(f+".doc");
                    BufferedWriter writer = new BufferedWriter(out);
                    DefaultTableModel model = (DefaultTableModel) tblMh.getModel();
                    
                    writer.write("Công ty trách nhiệm hữu hạn Trần Thế Lâm\n\n");
                    writer.write("\n----------DANH SÁCH HÀNG TRONG KHO------------\n\n\n\n");
                    for (int j  = 0;j<model.getColumnCount();j++){
                        writer.write(model.getColumnName(j)+"\t");
                    }
                    writer.write("\n\n");
                    for(int j = 0;j<model.getRowCount();j++){
                        for (int k =0;k<model.getColumnCount();k++){
                            writer.write(model.getValueAt(j, k) + "\t\t\t");
                        }
                        writer.write("\n\n");
                    }
                    writer.write("Thủ kho : "+namTK.getText());
                    writer.close();
                    JOptionPane.showMessageDialog(this, "Lưu thành công");
            }catch(Exception e){
                    e.printStackTrace();
            }
        }
        
    }//GEN-LAST:event_mnuXuatDSActionPerformed

