    public void showData(List<LichSpa> list){
        Vector col = new Vector();
        col.addElement("Ma Lich");
        col.addElement("Thoi gian"); 
        
        
       Vector data = new Vector();
       for (int i = 0; i< list.size(); i++) {
           Vector mh = new Vector();
           mh.addElement(list.get(i).getMalsp());
           mh.addElement(list.get(i).getThoigian());
           data.add(mh);
       }
      
        tbShowLichTrong.setModel(new DefaultTableModel(data,col));
         
    }
