    private void btnTimSdtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimSdtActionPerformed
        // TODO add your handling code here:
        
        if (tfSdt.getText().trim().length() != 0) {
            KhachHang kh  = khdb.get_by_sodt(tfSdt.getText().trim());
            if (kh != null) {
                JOptionPane.showMessageDialog(HienThiLichTrongFrame.this, "Khach hang : " + kh.getTen()  ,
                        "Thong tin khach hang" , JOptionPane.INFORMATION_MESSAGE);
                btThemmoi.setEnabled(false);
            }else {
                JOptionPane.showMessageDialog(HienThiLichTrongFrame.this, "Khach hang moi" ,
                        "Yeu cau them moi" , JOptionPane.WARNING_MESSAGE);
                btThemmoi.setEnabled(true);
            }
           
        }
    }//GEN-LAST:event_btnTimSdtActionPerformed

    private void btThemmoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemmoiActionPerformed
        try {
            // TODO add your handling code here:
            //insert kh vao db
            String ten = tfTen.getText();
            String email = tfEmail.getText();
            String sdt = tfSdt.getText();
            String diachi = tfDiachi.getText();
            if (ten.length() == 0 || sdt.length() == 0) {
                JOptionPane.showMessageDialog(HienThiLichTrongFrame.this, "Nhap day du thong tin cho ten va so dien thoai"  ,
                        "Loi" , JOptionPane.ERROR_MESSAGE);
                return;
            }
            KhachHang kh = new KhachHang( ten, sdt, email, diachi);
            khdb.insert(kh);
            JOptionPane.showMessageDialog(HienThiLichTrongFrame.this, "Them moi thanh cong"  ,
                        "Thanh cong" , JOptionPane.PLAIN_MESSAGE);
        } catch (SQLException ex) {
            Logger.getLogger(HienThiLichTrongFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btThemmoiActionPerformed

    private void btHoanTatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btHoanTatActionPerformed
        // TODO add your handling code here:
        
        if (maLSpa != 0 && khdb.get_by_sodt(tfSdt.getText()) != null) {
            try {
                Lich l = new Lich(maLSpa, tfSdt.getText(), 0, NhanhKhachFrame.nv.getMaNV());
                LichDB ldb = new LichDB(l);
                NhanhKhachFrame.maL = ldb.insert();
                LichSpaDB lsdb = new LichSpaDB();
                lsdb.update(maLSpa, 1);
                HienThiLichTrongFrame.this.setVisible(false);
            } catch (SQLException ex) {
                Logger.getLogger(HienThiLichTrongFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(HienThiLichTrongFrame.this, "Kiem tra lai lich va thong tin khach hang"  ,
                        "Loi" , JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btHoanTatActionPerformed

